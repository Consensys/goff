// SetBigInt sets z to v (regular form) and returns z in Montgomery form
func (z *Element11) SetBigInt(v *big.Int) *Element11 {
	z.SetZero()

	zero := big.NewInt(0)
	q := element11ModulusBigInt()

	// copy input
	vv := new(big.Int).Set(v)

	// while v < 0, v+=q
	for vv.Cmp(zero) == -1 {
		vv.Add(vv, q)
	}
	// while v > q, v-=q
	for vv.Cmp(q) == 1 {
		vv.Sub(vv, q)
	}
	// if v == q, return 0
	if vv.Cmp(q) == 0 {
		return z
	}
	// v should
	vBits := vv.Bits()
	for i := 0; i < len(vBits); i++ {
		z[i] = uint64(vBits[i])
	}
	return z.ToMont()
}

// SetString creates a big.Int with s (in base 10) and calls SetBigInt on z
func (z *Element11) SetString(s string) *Element11 {
	x, ok := new(big.Int).SetString(s, 10)
	if !ok {
		panic("Element11.SetString failed -> can't parse number in base10 into a big.Int")
	}
	return z.SetBigInt(x)
}

