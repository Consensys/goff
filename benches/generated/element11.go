// Sqrt z = x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *Element11) Sqrt(x *Element11) *Element11 {
	// q  1 (mod 4)
	// see modSqrtTonelliShanks in math/big/int.go
	// using https://www.maa.org/sites/default/files/pdf/upload_library/22/Polya/07468342.di020786.02p0470a.pdf

	var y, b, t, w Element11
	// w = x^((s-1)/2))
	w.Exp(*x,
		2262524253915424029,
		6081403200718372629,
		17155547574052322722,
		14791226136099216991,
		7592472046941249133,
		5636020973594210147,
		17178976574719344544,
		12827808287770592321,
		7456621895821919905,
		2269915356108507492,
		492031384284361892,
	)

	// y = x^((s+1)/2)) = w * x
	y.Mul(x, &w)

	// b = x^s = w * w * x = y * x
	b.Mul(&w, &y)

	// g = nonResidue ^ s
	var g = Element11{
		5213758866121994067,
		8772802915931313245,
		2890376057240547103,
		8482474714665442104,
		4548459128592750868,
		12121780254188267750,
		7792085139679764461,
		6609744862093189275,
		11593379026893229189,
		6876368418311573296,
		5586070972634230402,
	}
	r := uint64(3)

	// compute legendre symbol
	// t = x^((q-1)/2) = r-1 squaring of x^s
	t = b
	for i := uint64(0); i < r-1; i++ {
		t.Square(&t)
	}
	if t.IsZero() {
		return z.SetZero()
	}
	if !((t[10] == 2701739776609971068) && (t[9] == 1149684899365966707) && (t[8] == 1195772291922734025) && (t[7] == 13785248486660732866) && (t[6] == 3675071820267523062) && (t[5] == 4114769582080791442) && (t[4] == 15295311529813750342) && (t[3] == 6296109561273398274) && (t[2] == 4424799841612221365) && (t[1] == 8309282387817143644) && (t[0] == 1386200169544637518)) {
		// t != 1, we don't have a square root
		return nil
	}
	for {
		var m uint64
		t = b

		// for t != 1
		for !((t[10] == 2701739776609971068) && (t[9] == 1149684899365966707) && (t[8] == 1195772291922734025) && (t[7] == 13785248486660732866) && (t[6] == 3675071820267523062) && (t[5] == 4114769582080791442) && (t[4] == 15295311529813750342) && (t[3] == 6296109561273398274) && (t[2] == 4424799841612221365) && (t[1] == 8309282387817143644) && (t[0] == 1386200169544637518)) {
			t.Square(&t)
			m++
		}

		if m == 0 {
			return z.Set(&y)
		}
		// t = g^(2^(r-m-1)) mod q
		ge := int(r - m - 1)
		t = g
		for ge > 0 {
			t.Square(&t)
			ge--
		}

		g.Square(&t)
		y.MulAssign(&t)
		b.MulAssign(&g)
		r = m
	}
}

// Square z = x * x mod q
func (z *Element11) Square(x *Element11) *Element11 {
	return z.Mul(x, x)
}
